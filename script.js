let generateNewQuote = (current_quote) => {
  document.querySelector(".quote-text").textContent = current_quote.text;
  document.querySelector(".quote-author").textContent = current_quote.author;

  // Handling reaction counter
  {
    if (current_quote.reaction["like"] > 1) {
      document.querySelector(
        "#btn-like"
      ).textContent = `👍 ${current_quote.reaction["like"]}`;
      document.querySelector("#btn-like").style.backgroundColor = "green";
    } else {
      document.querySelector("#btn-like").style.backgroundColor = "transparent";
      document.querySelector("#btn-like").textContent = `👍`;
    }

    if (current_quote.reaction["right"] > 1) {
      document.querySelector(
        "#btn-right"
      ).textContent = `💯 ${current_quote.reaction["right"]}`;
      document.querySelector("#btn-right").style.backgroundColor = "violet";
    } else {
      document.querySelector("#btn-right").style.backgroundColor =
        "transparent";
      document.querySelector("#btn-right").textContent = `💯`;
    }

    if (current_quote.reaction["love"] > 1) {
      document.querySelector(
        "#btn-love"
      ).textContent = `❤️ ${current_quote.reaction["love"]}`;
      document.querySelector("#btn-love").style.backgroundColor = "violet";
    } else {
      document.querySelector("#btn-love").style.backgroundColor = "transparent";
      document.querySelector("#btn-love").textContent = `❤️`;
    }

    if (current_quote.reaction["dislike"] > 1) {
      document.querySelector(
        "#btn-dislike"
      ).textContent = `👎 ${current_quote.reaction["dislike"]}`;
      document.querySelector("#btn-dislike").style.backgroundColor = "red";
    } else {
      document.querySelector("#btn-dislike").style.backgroundColor =
        "transparent";
      document.querySelector("#btn-dislike").textContent = `👎`;
    }
    if (current_quote.reaction["vomit"] > 1) {
      document.querySelector(
        "#btn-vomit"
      ).textContent = `🤮 ${current_quote.reaction["vomit"]}`;
      document.querySelector("#btn-vomit").style.backgroundColor = "yellow";
    } else {
      document.querySelector("#btn-vomit").style.backgroundColor =
        "transparent";
      document.querySelector("#btn-vomit").textContent = `🤮`;
    }
  }
};
